#include "libft.h"
#include <stdio.h>

char	*ft_strjoin2(char *s1, const char *s2)
{
	char	*res;
	int		i;
	int		j;

	if (!s2)
		return (NULL);
	i = 0;
	j = 0;
	res = malloc(sizeof(char) * (ft_strlen(s1) + ft_strlen(s2) + 1));
	if (!res)
		return (NULL);
	while (s1 && s1[i] != '\0')
	{
		res[i] = s1[i];
		i++;
	}
	while (s2[j] != '\0')
	{
		res[i + j] = s2[j];
		j++;
	}
	res[i + j] = '\0';
	free(s1);
	s1 = NULL;
	return (res);
}
